package com.javapros.classone;

public class Primitives {

	public static void main(String args[]) {

		/**
		 * This is declaring and printing an integer value
		 */
		int x = 3;
		System.out.println("integer value is :" + x);

		/**
		 * This is declaring and printing float value
		 */
		float y = 3.45f;
		System.out.println("float value is:" + y);

		/**
		 * This is for declaring and printing long integer value
		 */
		long z = 40000000;
		System.out.println("long value is:" + z);

		/**
		 * This is for declaring double value
		 */
		double p = 40.00000f;
		System.out.println("double value" + p);

		/**
		 * This is to print string value (couln't use char not sure why)
		 */
		String q = "Murali@123";
		System.out.println("string value:" + q);

		/**
		 * Printing boolean
		 */

	}

}
